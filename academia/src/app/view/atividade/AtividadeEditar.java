    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        String nome, valor_string,ativo;
        Float valor;
        String msg = "";

        Validador.valida_textField(nomeTxt, 100, true, false, 0, "Nome");
        Validador.valida_textField(valorTxt, 0, true, false, 1, "Valor");

        if (Validador.erros == 0) {

            nome = nomeTxt.getText();
            valor_string = valorTxt.getText().replace(".", "");
            valor_string = valor_string.replace(",", ".");
            valor = Float.parseFloat(valor_string);

            if (ativoCbox.isSelected()){
                ativo = "S";
            }else {
                ativo = "N";
            }

            ac = new AtividadeController();
            Atividade a = new Atividade(nome, ativo, valor);
            ac.salvarEdicao( a,AtividadeView.id);
            this.setVisible(false);
            this.dispose();

        } else {

            for (int i = 0; i < 9; i++) {//Lista os erros
                for (int j = 0; j < 3; j++) {
                    if (null != Validador.arrayErros[i][j]) {
                        msg = msg + Validador.arrayErros[i][j];
                    }

                }
            }

            JOptionPane.showMessageDialog(null, msg, "Erros", JOptionPane.ERROR_MESSAGE);
            Validador.erros = 0;
            for (int i = 0; i < 9; i++) {// Apaga os erros para nÃ£o se acumular.
                for (int j = 0; j < 3; j++) {
                    if (null != Validador.arrayErros[i][j]) {
                        Validador.arrayErros[i][j] = "";
                    }

                }
            }
        }
    }//GEN-LAST:event_btAlterarActionPerformed

